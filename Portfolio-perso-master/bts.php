                    <h2 style="text-align: center; font-size: 20px; font-weight: normal;" data-aos="zoom-in">Option SLAM : Solutions Logicielles et Applications Métiers
                        Cette option est orientée vers le développement de logiciels et la création d'applications. Les compétences acquises permettent de concevoir, réaliser et 
                        maintenir des programmes, applications et sites web adaptés aux besoins des utilisateurs.
                        <br><br>
                        Les étudiants en SLAM se forment notamment sur :
                        <br><br>
                        Le développement d'applications web et mobiles. <br>
                        La programmation orientée objet (Java, C#, Python, etc.). <br>
                        La gestion de bases de données (SQL). <br>
                        La conception et la modélisation d'applications (UML, Merise). <br>
                        La gestion des projets informatiques. <br>
                        Ce parcours est idéal pour ceux qui se destinent à des métiers tels que développeur web, développeur d'applications ou chef de projet en développement 
                        logiciel.</h2>
