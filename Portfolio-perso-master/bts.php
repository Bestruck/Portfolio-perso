                    <h2 style="text-align: center; font-size: 20px; font-weight: normal;" data-aos="zoom-in">Option SISR : Solutions d'Infrastructure, Systèmes et Réseaux
                        L'option SISR se concentre sur l'administration des réseaux et systèmes informatiques. Les étudiants apprennent à installer, configurer, sécuriser et maintenir 
                        les infrastructures réseau des organisations.
                        <br> <br>
                        Les compétences acquises en SISR incluent :
                        <br><br>
                        L'administration de serveurs (Windows Server, Linux). <br>
                        La gestion des réseaux (câblage, routage, sécurité). <br>
                        La virtualisation et la gestion des machines virtuelles. <br> 
                        La sécurité des systèmes d'information (firewalls, antivirus, sauvegarde). <br>
                        La gestion des parcs informatiques. <br>
                        Ce parcours est adapté à ceux qui souhaitent devenir administrateurs réseau, techniciens de maintenance, responsables de la sécurité des systèmes ou 
                        administrateurs systèmes.</h2>   
